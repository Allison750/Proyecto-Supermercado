package Arreglos;

import java.util.ArrayList;

import Clases.Login;

public class ArregloLogin {
	private ArrayList<Login> login;
	public ArregloLogin(){
		login=new ArrayList<Login>();
	}
    //MÉTODO ADICIONAR
	public void Adicionar(Login x){
		login.add(x);
	}
	//MÉTODO TAMAÑO DEL ARRAY
	public int Tamaño(){
		return login.size();
	}
	//MÉTODO OBTENER
	public Login Obtener(int i) {
		return login.get(i);
	}
   //MÉTODO BUSCAR
	
	public Login Buscar(String cod){
        Login e;
        for(int i=0; i<Tamaño(); i++){
            e = Obtener(i);
            if(e.getUsuario().equals(cod)){
                return e;
            }
        }
        return null;
    }

   //MÉTODO ELIMINAR
	public void Eliminar(Login x) {
		login.remove(x);
	}
}
